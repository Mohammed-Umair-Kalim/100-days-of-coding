"use strict";
// Question 115: Use a switch statement to log the days of the week based on a number (1-7) ?
// Explain & TIP: A switch statement is a great way to select one of many code blocks to be executed.
// It's particularly useful for scenarios like mapping numbers to days of the week.
// Answer 115:
let days = 2;
switch (days) {
    case 1:
        console.log("\nFriday");
        break;
    case 2:
        console.log("\nSaturday");
        break;
    case 3:
        console.log("\nSunday");
        break;
    case 4:
        console.log("\nMonday");
        break;
    case 5:
        console.log("\nTuseday");
        break;
    case 6:
        console.log("\nWednesday");
        break;
    case 7:
        console.log("\nThursday");
        break;
}
